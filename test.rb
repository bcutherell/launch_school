# What code could we add to see each student printed with their respective grade?

students = ['Martin', 'Milo', 'Michelle']
grades = [94, 87, 98]

index = 0
while index < students.length
  puts '------------'
  puts students[index]
  puts grades[index]
  index += 1
end








